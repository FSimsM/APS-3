package src;

public class InstanciaRaiz {
	
	private final double numero;
	private final double indice;

	public InstanciaRaiz(final double numero, final double indice) {
		this.numero = numero;
		this.indice = indice;
	}
	
	public double getNumero() {
		return numero;
	}
	public double getIndice() {
		return indice;
	}
	

}
